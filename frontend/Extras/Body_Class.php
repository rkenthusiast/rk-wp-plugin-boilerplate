namespace rk_wp_plugin_boilerplate\Frontend\Extras;

use rk_wp_plugin_boilerplate\Engine\Base;

/**
 * Add custom css class to <body>
 */
class Body_Class extends Base {

	/**
	 * Initialize the class.
	 *
	 * @return void|bool
	 */
	public function initialize() {
		parent::initialize();

		\add_filter( 'body_class', array( self::class, 'add_r_class' ), 10, 1 );
	}

	/**
	 * Add class in the body on the frontend
	 *
	 * @param array $classes The array with all the classes of the page.
	 * @since 1.0.0
	 * @return array
	 */
	public static function add_r_class( array $classes ) {
		$classes[] = R_TEXTDOMAIN;

		return $classes;
	}

}
